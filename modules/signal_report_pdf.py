# ── Drawing helpers ─────────────────────────────────────


def _draw_progress_bar(canvas_obj, x, y, width, height, filled_ratio, accent):
    """Draw a rounded progress bar. Used for each signal score."""
    filled_ratio = max(0, min(1, filled_ratio))
    # Track
    canvas_obj.setFillColor(HexColor(RULE))
    canvas_obj.roundRect(x, y, width, height, height / 2, stroke=0, fill=1)
    # Fill
    if filled_ratio > 0:
        fill_width = max(height, width * filled_ratio)
        canvas_obj.setFillColor(accent)
        canvas_obj.roundRect(x, y, fill_width, height, height / 2, stroke=0, fill=1)


def _draw_sparkline(canvas_obj, x, y, width, height, values, accent):
    """Draw a mini sparkline chart from a list of numeric values.
    Used on Pro/Agency variants for the 14-day history per signal.
    Silently renders nothing if there are fewer than 2 data points."""
    if not values or len(values) < 2:
        # Draw a baseline only
        canvas_obj.setStrokeColor(HexColor(RULE))
        canvas_obj.setLineWidth(0.5)
        canvas_obj.line(x, y + height / 2, x + width, y + height / 2)
        return

    clean = [v for v in values if v is not None]
    if len(clean) < 2:
        canvas_obj.setStrokeColor(HexColor(RULE))
        canvas_obj.line(x, y + height / 2, x + width, y + height / 2)
        return

    v_min = min(clean)
    v_max = max(clean)
    v_range = v_max - v_min if v_max > v_min else 1

    # Scale x across count-1 segments
    n = len(values)
    x_step = width / (n - 1)

    # Build line path
    points = []
    for i, v in enumerate(values):
        if v is None:
            continue
        px = x + i * x_step
        py = y + ((v - v_min) / v_range) * height
        points.append((px, py))

    canvas_obj.setStrokeColor(accent)
    canvas_obj.setLineWidth(1.2)
    canvas_obj.setLineCap(1)
    canvas_obj.setLineJoin(1)
    for i in range(len(points) - 1):
        canvas_obj.line(points[i][0], points[i][1], points[i + 1][0], points[i + 1][1])

    # End dot
    if points:
        canvas_obj.setFillColor(accent)
        canvas_obj.circle(points[-1][0], points[-1][1], 1.5, stroke=0, fill=1)


def _draw_score_card(canvas_obj, doc, score_value, grade, accent):
    """Top-of-page-1 hero block with the big score and grade. Drawn as
    a canvas overlay so we get control over absolute position instead
    of being at the mercy of flowable layout."""
    page_width = LETTER[0]
    left = doc.leftMargin
    right = page_width - doc.rightMargin
    width = right - left
    top = LETTER[1] - 1.5 * inch

    # Card background
    card_h = 1.6 * inch
    card_y = top - card_h
    canvas_obj.setFillColor(HexColor(PAPER_ALT))
    canvas_obj.setStrokeColor(HexColor(RULE))
    canvas_obj.setLineWidth(0.75)
    canvas_obj.roundRect(left, card_y, width, card_h, 12, stroke=1, fill=1)

    # Giant score on the left
    canvas_obj.setFillColor(HexColor(INK))
    canvas_obj.setFont("Helvetica-Bold", 64)
    score_text = f"{int(round(score_value))}"
    score_text_width = canvas_obj.stringWidth(score_text, "Helvetica-Bold", 64)
    score_x = left + 0.5 * inch
    score_y = card_y + 0.35 * inch
    canvas_obj.drawString(score_x, score_y, score_text)

    # " / 100" suffix
    canvas_obj.setFont("Helvetica", 16)
    canvas_obj.setFillColor(HexColor(INK_FAINT))
    canvas_obj.drawString(score_x + score_text_width + 6, score_y + 4, "/ 100")

    # "Signal Score" label under the score
    canvas_obj.setFont("Helvetica-Bold", 8)
    canvas_obj.setFillColor(HexColor(INK_MUTE))
    canvas_obj.drawString(score_x, card_y + 0.18 * inch, "SIGNAL SCORE")

    # Grade pill on the right
    grade_color = HexColor(GRADE_COLORS.get(grade, INK))
    pill_w = 1.1 * inch
    pill_h = 0.7 * inch
    pill_x = right - pill_w - 0.5 * inch
    pill_y = card_y + (card_h - pill_h) / 2
    canvas_obj.setFillColor(grade_color)
    canvas_obj.roundRect(pill_x, pill_y, pill_w, pill_h, 10, stroke=0, fill=1)
    canvas_obj.setFillColor(HexColor("#ffffff"))
    canvas_obj.setFont("Helvetica-Bold", 32)
    grade_text_w = canvas_obj.stringWidth(grade, "Helvetica-Bold", 32)
    canvas_obj.drawString(pill_x + (pill_w - grade_text_w) / 2, pill_y + 0.18 * inch, grade)
    canvas_obj.setFont("Helvetica-Bold", 7)
    canvas_obj.drawString(pill_x + 0.12 * inch, pill_y - 0.16 * inch, "GRADE")

    # Grade label between score and pill
    label_copy = SIGNAL_GRADE_COPY.get(grade, {})
    label = label_copy.get("label", "")
    if label:
        canvas_obj.setFillColor(HexColor(INK_MUTE))
        canvas_obj.setFont("Helvetica", 10)
        canvas_obj.drawString(score_x, card_y + 0.02 * inch - 6, label)


# ── Header and footer rendering ─────────────────────────


def _draw_header_footer(canvas_obj, doc, user, variant, accent, score_row):
    """Page decorator. Draws the brand strip at the top and the footer
    line at the bottom on every page. Agency variant uses the uploaded
    logo text (no raster image rendering yet — v1 defers that) and can
    hide the InbXr attribution."""
    canvas_obj.saveState()
    page_width = LETTER[0]
    page_height = LETTER[1]

    # ── Top brand strip ──
    strip_y = page_height - 0.45 * inch
    canvas_obj.setFillColor(accent)
    canvas_obj.rect(0, strip_y, page_width, 0.08 * inch, stroke=0, fill=1)

    # Brand label (either InbXr or agency custom text)
    if variant == "agency":
        brand_label = (user.get("agency_footer_text") or "").strip() or user.get(
            "display_name"
        ) or "Signal Report"
    else:
        brand_label = "InbXr · Signal Report"

    canvas_obj.setFillColor(HexColor(INK))
    canvas_obj.setFont("Helvetica-Bold", 10)
    canvas_obj.drawString(doc.leftMargin, strip_y + 0.22 * inch, brand_label)

    # Date on the right
    generated = datetime.utcnow().strftime("%B %d, %Y")
    canvas_obj.setFont("Helvetica", 9)
    canvas_obj.setFillColor(HexColor(INK_MUTE))
    date_w = canvas_obj.stringWidth(generated, "Helvetica", 9)
    canvas_obj.drawString(page_width - doc.rightMargin - date_w, strip_y + 0.22 * inch, generated)

    # ── Bottom footer ──
    footer_y = 0.4 * inch

    canvas_obj.setStrokeColor(HexColor(RULE))
    canvas_obj.setLineWidth(0.5)
    canvas_obj.line(doc.leftMargin, footer_y + 0.18 * inch, page_width - doc.rightMargin, footer_y + 0.18 * inch)

    # Left side: "Generated by InbXr Signal Engine" unless hidden
    hide_brand = variant == "agency" and bool(user.get("agency_hide_inbxr_brand"))
    if not hide_brand:
        canvas_obj.setFont("Helvetica", 7.5)
        canvas_obj.setFillColor(HexColor(INK_FAINT))
        canvas_obj.drawString(
            doc.leftMargin,
            footer_y,
            "Generated by the Signal Engine  ·  https://inbxr.us",
        )

    # Right side: page number
    page_num = canvas_obj.getPageNumber()
    canvas_obj.setFont("Helvetica", 7.5)
    canvas_obj.setFillColor(HexColor(INK_FAINT))
    page_text = f"Page {page_num}"
    pw = canvas_obj.stringWidth(page_text, "Helvetica", 7.5)
    canvas_obj.drawString(page_width - doc.rightMargin - pw, footer_y, page_text)

    canvas_obj.restoreState()


# ── Story builders for each content section ────────────


def _build_hero_flowables(score_row, styles):
    """Build the top flowables that sit below the canvas-drawn score card.
    We leave vertical space for the card via a Spacer that matches its
    height + a small breathing gap."""
    grade = score_row.get("signal_grade", "F")
    grade_copy = SIGNAL_GRADE_COPY.get(grade, {})
    total = score_row.get("total_signal_score") or 0
    total_contacts = score_row.get("total_contacts") or 0

    story = []
    # Reserve space for the canvas-drawn score card
    story.append(Spacer(1, 1.7 * inch))
    story.append(Paragraph("SIGNAL SCORE SUMMARY", styles["eyebrow"]))

    headline = grade_copy.get("description", "")
    if headline:
        story.append(Paragraph(headline, styles["body"]))

    contacts_line = (
        f"<b>{total_contacts:,}</b> contacts scored through the Signal Engine. "
        f"Report generated from the most recent scan."
    )
    story.append(Paragraph(contacts_line, styles["body_small"]))
    return story


def _build_signals_table(score_row, styles, accent, include_sparklines, history):
    """Build the 7-signal breakdown as a single Table flowable.

    Each row has: signal number + name, weight, score fraction, progress bar.
    For Pro/Agency variants with history, we pass include_sparklines=True
    and render a small sparkline column.
    """
    rows = []

    # Header
    header_cells = [
        Paragraph("<b>Signal</b>", styles["body_small"]),
        Paragraph("<b>Weight</b>", styles["signal_weight"]),
        Paragraph("<b>Score</b>", styles["signal_weight"]),
        Paragraph("<b>Reading</b>", styles["body_small"]),
    ]
    if include_sparklines:
        header_cells.append(Paragraph("<b>14d</b>", styles["signal_weight"]))
    rows.append(header_cells)

    for key in SIGNAL_ORDER:
        meta = SIGNAL_DIMENSION_COPY.get(key, {})
        max_pts = SIGNAL_MAX_POINTS[key]
        value = score_row.get(f"{key}_score") or 0
        number = meta.get("number", "")
        name = meta.get("name", key)
        note = meta.get("note", "")

        name_cell = Paragraph(
            f"<b>{number} · {name}</b><br/><font size='7' color='{INK_MUTE}'>{note}</font>",
            styles["body_small"],
        )
        weight_cell = Paragraph(f"{max_pts} pts", styles["signal_weight"])
        score_cell = Paragraph(
            f"<b>{value:.1f}</b> <font color='{INK_FAINT}'>/ {max_pts}</font>",
            styles["signal_weight"],
        )

        bar_ratio = (value / max_pts) if max_pts else 0
        # Reading column: progress bar drawn as a custom flowable via a Table trick
        # We stash the ratio so the TableStyle canvas callback can paint it.
        reading_cell = _ProgressBarFlow(bar_ratio, accent)

        cells = [name_cell, weight_cell, score_cell, reading_cell]

        if include_sparklines:
            # Extract the corresponding history column
            hist_values = [h.get(f"{key}_score") for h in history] if history else []
            cells.append(_SparklineFlow(hist_values, accent))

        rows.append(cells)

    col_widths = [2.0 * inch, 0.55 * inch, 0.7 * inch, 2.7 * inch]
    if include_sparklines:
        col_widths = [1.75 * inch, 0.5 * inch, 0.65 * inch, 2.05 * inch, 1.0 * inch]

    table = Table(rows, colWidths=col_widths, repeatRows=1)
    table.setStyle(
        TableStyle([
            ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
            ("LINEBELOW", (0, 0), (-1, 0), 0.5, HexColor(RULE)),
            ("LINEBELOW", (0, 1), (-1, -2), 0.25, HexColor(RULE)),
            ("TOPPADDING", (0, 1), (-1, -1), 8),
            ("BOTTOMPADDING", (0, 1), (-1, -1), 8),
            ("LEFTPADDING", (0, 0), (-1, -1), 4),
            ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ])
    )
    return table


def _build_recommendations(score_row, styles, accent):
    """Top 3 recommendations based on the weakest signals."""
    # Rank signals by ratio
    ratios = []
    for key in SIGNAL_ORDER:
        max_pts = SIGNAL_MAX_POINTS[key]
        value = score_row.get(f"{key}_score") or 0
        ratios.append((key, value / max_pts if max_pts else 1))
    ratios.sort(key=lambda x: x[1])

    story = [
        Paragraph("RECOMMENDED ACTIONS", styles["eyebrow"]),
        Paragraph("Fix these three first", styles["section_h"]),
    ]

    for rank, (key, ratio) in enumerate(ratios[:3], start=1):
        meta = SIGNAL_DIMENSION_COPY.get(key, {})
        action = ACTION_RECOMMENDATIONS.get(key, {})
        name = meta.get("name", key)
        action_label = action.get("label", f"Investigate {name}")
        action_feature = action.get("feature", "")

        bullet = (
            f"<b>{rank}. {name}</b> &mdash; {action_label}"
            + (f" <font color='{INK_FAINT}'>({action_feature})</font>" if action_feature else "")
        )
        story.append(Paragraph(bullet, styles["body"]))

    return story


