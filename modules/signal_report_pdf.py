# ── Top-level entry point ───────────────────────────────


def generate_pdf(user_id, score_id=None, variant=None):
    """Build a Signal Report PDF for a user.

    Returns (bytes, metadata_dict) on success, or (None, error_dict) on
    failure. The metadata dict includes variant, score, grade, and
    byte count so the caller can log the generation and enforce rate
    limits against the pdf_generations table.
    """
    user = _load_user(user_id)
    if not user:
        return None, {"error": "user_not_found"}

    score_row = _load_latest_score(user_id, score_id)
    if not score_row:
        return None, {"error": "no_score_yet"}

    # Resolve variant
    if variant is None:
        variant = _resolve_variant(user, score_row)

    accent = _brand_color_for(user, variant)
    styles = _styles(accent)

    history = []
    if variant in ("pro", "agency"):
        history = _load_history(user_id, limit=14)

    # Build the story
    story = []
    story.extend(_build_hero_flowables(score_row, styles))
    story.append(Paragraph("THE 7 INBOX SIGNALS", styles["eyebrow"]))
    story.append(Paragraph("Signal breakdown", styles["section_h"]))
    story.append(
        _build_signals_table(
            score_row,
            styles,
            accent,
            include_sparklines=(variant in ("pro", "agency")),
            history=history,
        )
    )
    story.append(Spacer(1, 0.2 * inch))
    story.extend(_build_recommendations(score_row, styles, accent))

    if variant in ("pro", "agency"):
        story.extend(_build_segments_page(score_row, styles))

    # Build the PDF
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=LETTER,
        leftMargin=0.65 * inch,
        rightMargin=0.65 * inch,
        topMargin=1.0 * inch,
        bottomMargin=0.8 * inch,
        title=f"Signal Report",
        author="InbXr",
    )

    grade = score_row.get("signal_grade", "F")
    score_value = score_row.get("total_signal_score") or 0

    def _on_page(canvas_obj, doc_ref):
        _draw_header_footer(canvas_obj, doc_ref, user, variant, accent, score_row)
        if canvas_obj.getPageNumber() == 1:
            _draw_score_card(canvas_obj, doc_ref, score_value, grade, accent)

    try:
        doc.build(story, onFirstPage=_on_page, onLaterPages=_on_page)
    except Exception:
        logger.exception("[PDF] Build failed for user_id=%s", user_id)
        return None, {"error": "build_failed"}

    pdf_bytes = buffer.getvalue()
    buffer.close()

    metadata = {
        "variant": variant,
        "score": int(round(score_value)),
        "grade": grade,
        "size_bytes": len(pdf_bytes),
    }
    return pdf_bytes, metadata


# ── Logging + rate limits ───────────────────────────────


def log_pdf_generation(user_id, tier, variant, score, size_bytes):
    """Insert a row into pdf_generations. Safe to call after every
    successful generation — failures to log should not block the
    user from downloading the PDF."""
    try:
        execute(
            "INSERT INTO pdf_generations "
            "(user_id, tier, variant, signal_score, file_size_bytes) "
            "VALUES (?, ?, ?, ?, ?)",
            (user_id, tier, variant, score, size_bytes),
        )
    except Exception:
        logger.exception("[PDF] Failed to log generation for user %s", user_id)


def count_pdfs_today(user_id):
    """How many PDFs this user has generated since UTC midnight.
    Used by the route handler to enforce daily limits from tiers.py."""
    try:
        row = fetchone(
            "SELECT COUNT(*) AS n FROM pdf_generations "
            "WHERE user_id = ? AND date(generated_at) = date('now')",
            (user_id,),
        )
        return row["n"] if row else 0
    except Exception:
        logger.exception("[PDF] count_pdfs_today failed")
        return 0
