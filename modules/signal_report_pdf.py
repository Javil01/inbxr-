def _load_user(user_id):
    """Load user row including agency white-label settings."""
    return fetchone(
        "SELECT id, email, display_name, tier, "
        "agency_logo_url, agency_primary_color, agency_footer_text, "
        "agency_hide_inbxr_brand "
        "FROM users WHERE id = ?",
        (user_id,),
    )


def _load_latest_score(user_id, score_id=None):
    """Load the score row the report will render. If score_id is given,
    load that specific row (used by shared links). Otherwise load the
    most recent score for the user."""
    if score_id:
        return fetchone(
            "SELECT * FROM signal_scores WHERE id = ? AND user_id = ?",
            (score_id, user_id),
        )
    return fetchone(
        "SELECT * FROM signal_scores WHERE user_id = ? "
        "ORDER BY calculated_at DESC LIMIT 1",
        (user_id,),
    )


def _load_history(user_id, limit=14):
    """Last N days of signal history. Used by Pro/Agency variants for
    sparklines and the 14-day trend comparison. Returns oldest-first
    so the sparkline draws left to right."""
    rows = fetchall(
        "SELECT total_signal_score, bounce_exposure_score, "
        "engagement_trajectory_score, acquisition_quality_score, "
        "domain_reputation_score, dormancy_risk_score, "
        "authentication_standing_score, decay_velocity_score, "
        "recorded_at "
        "FROM signal_score_history WHERE user_id = ? "
        "ORDER BY recorded_at DESC LIMIT ?",
        (user_id, limit),
    )
    return list(reversed(rows)) if rows else []


def _weakest_signal(score_row):
    """Identify the weakest signal by percentage of its weight."""
    ratios = []
    for key in SIGNAL_ORDER:
        max_pts = SIGNAL_MAX_POINTS[key]
        value = score_row.get(f"{key}_score") or 0
        ratios.append((key, value / max_pts if max_pts else 0))
    ratios.sort(key=lambda x: x[1])
    return ratios[0][0] if ratios else None


def _resolve_variant(user, score_row):
    """Pick which PDF variant to render for this user.

    Agency users get the white-label variant IF they have intelligence
    access. Pro users get history charts. Free users get the basic
    one-page report. Honors the tier override from the score row so
    old scores keep their original tier framing."""
    tier = user.get("tier", "free")
    if tier == "agency" and has_feature(tier, "signal_pdf_white_label"):
        return "agency"
    if has_feature(tier, "signal_pdf_history"):
        return "pro"
    return "free"


def _brand_color_for(user, variant):
    """Return the accent HexColor for this user's variant. Agency users
    with a custom primary color override the default brand blue. All
    other variants use the default."""
    if variant == "agency":
        custom = (user.get("agency_primary_color") or DEFAULT_BRAND).strip()
        if custom.startswith("#") and len(custom) in (4, 7):
            try:
                return HexColor(custom)
            except Exception:
                pass
    return HexColor(DEFAULT_BRAND)


